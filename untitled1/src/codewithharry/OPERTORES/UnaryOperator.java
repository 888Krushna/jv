package codewithharry.OPERTORES;

public class UnaryOperator {
    public static void main(String[] args) {
        int x= 10;

         // postfix
        System.out.println(x++);
        // prefix
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(--x);

        System.out.println(x++);
        System.out.println(++x);
        System.out.println(x--);
        System.out.println(--x);



    }
}
